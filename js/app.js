let currentLang = localStorage.getItem('clovedirect_lang') || 'EN';

function updateLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            // Check if it's an input/textarea placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.setAttribute('placeholder', i18n[lang][key]);
            } else {
                el.textContent = i18n[lang][key];
            }
        }
    });

    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        langToggleBtn.innerHTML = lang === 'EN' 
            ? '<span id="current-lang">EN</span> / <span class="opacity-50">ID</span>' 
            : '<span class="opacity-50">EN</span> / <span id="current-lang">ID</span>';
    }
}

// Mobile Menu Logic
function setupMobileMenu() {
    const btn = document.getElementById('mobile-menu-btn');
    const menu = document.getElementById('mobile-menu');
    const icon = document.getElementById('menu-icon');
    
    if (btn && menu) {
        // Remove old event listeners if they exist (to avoid duplicates)
        const newBtn = btn.cloneNode(true);
        btn.parentNode.replaceChild(newBtn, btn);
        
        let isOpen = false;
        
        newBtn.addEventListener('click', () => {
            isOpen = !isOpen;
            if (isOpen) {
                menu.classList.remove('-translate-y-full');
                icon.textContent = 'close';
            } else {
                menu.classList.add('-translate-y-full');
                icon.textContent = 'menu';
            }
        });
        
        // Close menu when a link is clicked
        const mobileLinks = menu.querySelectorAll('.nav-link.mobile');
        mobileLinks.forEach(link => {
            link.addEventListener('click', () => {
                isOpen = false;
                menu.classList.add('-translate-y-full');
                icon.textContent = 'menu';
            });
        });
    }
}

async function loadComponent(id, url) {
    try {
        const response = await fetch(url);
        if (response.ok) {
            const html = await response.text();
            document.getElementById(id).innerHTML = html;
            
            updateLanguage(currentLang);
            
            if (id === 'navbar-placeholder') {
                const langToggleBtn = document.getElementById('lang-toggle');
                if (langToggleBtn) {
                    langToggleBtn.addEventListener('click', () => {
                        currentLang = currentLang === 'EN' ? 'ID' : 'EN';
                        localStorage.setItem('clovedirect_lang', currentLang);
                        updateLanguage(currentLang);
                    });
                }
                updateActiveNav();
                setupMobileMenu(); // Re-setup mobile menu logic after loading navbar
            }
        }
    } catch (e) {
        console.error('Failed to load component:', url, e);
    }
}

async function loadPage(page) {
    try {
        const response = await fetch(`${page === 'index' ? 'index' : page}.html`);
        const text = await response.text();
        
        const parser = new DOMParser();
        const doc = parser.parseFromString(text, 'text/html');
        const mainContent = doc.querySelector('main').innerHTML;
        
        if (document.startViewTransition) {
            document.startViewTransition(() => {
                document.querySelector('main').innerHTML = mainContent;
                updateLanguage(currentLang);
                updateActiveNav();
                window.scrollTo({ top: 0, behavior: 'instant' });
            });
        } else {
            document.querySelector('main').innerHTML = mainContent;
            updateLanguage(currentLang);
            updateActiveNav();
            window.scrollTo({ top: 0, behavior: 'instant' });
        }
    } catch(e) {
        console.error("Error loading page", e);
    }
}

document.addEventListener('click', (e) => {
    const link = e.target.closest('a');
    if (!link) return;
    
    // Normal hash link
    if (link.getAttribute('href').startsWith('#')) return;
    if (link.getAttribute('href').startsWith('mailto:')) return;
    if (link.getAttribute('href').startsWith('tel:')) return;
    
    if (link.classList.contains('nav-link') || link.classList.contains('logo-link')) {
        e.preventDefault();
        const page = link.getAttribute('data-page');
        window.history.pushState({page}, '', `${page === 'index' ? 'index.html' : page + '.html'}`);
        loadPage(page);
    }
});

function updateActiveNav() {
    const currentPath = window.location.pathname;
    let page = 'index';
    if (currentPath.includes('about')) page = 'about';
    if (currentPath.includes('product')) page = 'product';
    if (currentPath.includes('contact')) page = 'contact';

    // Handle desktop links
    document.querySelectorAll('nav .nav-link:not(.mobile)').forEach(link => {
        const line = link.querySelector('.nav-line');
        if (line) {
            line.classList.remove('scale-x-100');
            line.classList.add('scale-x-0');
        }
        
        if (link.dataset.page === page) {
            if (line) {
                line.classList.remove('scale-x-0');
                line.classList.add('scale-x-100');
            }
            link.classList.add('text-primary');
            link.classList.remove('text-on-surface-variant');
        } else {
            link.classList.remove('text-primary');
            link.classList.add('text-on-surface-variant');
        }
    });

    // Handle mobile links
    document.querySelectorAll('nav .nav-link.mobile').forEach(link => {
        if (link.dataset.page === page) {
            link.classList.add('text-primary');
            link.classList.remove('text-on-surface-variant');
        } else {
            link.classList.remove('text-primary');
            link.classList.add('text-on-surface-variant');
        }
    });
}

// History API support for back button
window.addEventListener('popstate', (e) => {
    if (e.state && e.state.page) {
        loadPage(e.state.page);
    } else {
        const path = window.location.pathname;
        let page = 'index';
        if (path.includes('about')) page = 'about';
        if (path.includes('product')) page = 'product';
        if (path.includes('contact')) page = 'contact';
        loadPage(page);
    }
});

document.addEventListener('DOMContentLoaded', () => {
    const bodyContent = document.getElementById('body-content');
    if (bodyContent) {
        bodyContent.classList.remove('opacity-0');
    }

    loadComponent('navbar-placeholder', 'components/navbar.html');
    loadComponent('footer-placeholder', 'components/footer.html');
    
    updateLanguage(currentLang);
});
